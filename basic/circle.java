package basic;

public class circle {
    public static void main(String[] args) {
        double r = 8;
        double area = 3.1415 * r * r;
        double circumference = 2 * 3.1415 * r;
        System.out.println("Area of the circle is: " + area);
        System.out.println("Circumference of the circle is: " + circumference);
    }
    
}
