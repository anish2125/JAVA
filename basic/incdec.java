package basic;

public class incdec {
    public static void main(String[] args) {
        int x =5;
        System.out.println(x);
        x++;  // x++ is use then increment
        System.out.println(x);
        ++x; // increment and then use
        System.out.println(x);
        x--;
        System.out.println(x);
    }
    
}
